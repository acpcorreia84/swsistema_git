					$get = 'get' . $tableMap->getColumn($colName)->getPhpName();
					$columns[$colName] = $obj->$get();
				}
			}
		} else {

		}

		return BasePeer::doValidate(ProspectMetaPeer::DATABASE_NAME, ProspectMetaPeer::TABLE_NAME, $columns);
	}

	/**
	 * Retrieve a single object by pkey.
	 *
	 * @param      int $pk the primary key.
	 * @param      PropelPDO $con the connection to use
	 * @return     ProspectMeta
	 */
	public static function retrieveByPK($pk, PropelPDO $con = null)
	{

		if (null !== ($obj = ProspectMetaPeer::getInstanceFromPool((string) $pk))) {
			return $obj;
		}

		if ($con === null) {
			$con = Propel::getConnection(ProspectMetaPeer::DATABASE_NAME, Propel::CONNECTION_READ);
		}

		$criteria = new Criteria(ProspectMetaPeer::DATABASE_NAME);
		$criteria->add(ProspectMetaPeer::ID, $pk);

		$v = ProspectMetaPeer::doSelect($criteria, $con);

		return !empty($v) > 0 ? $v[0] : null;
	}

