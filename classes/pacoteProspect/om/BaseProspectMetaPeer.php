	/**
	 * Selects a collection of ProspectMeta objects pre-filled with their ProspectTipo objects.
	 * @param      Criteria  $criteria
	 * @param      PropelPDO $con
	 * @param      String    $join_behavior the type of joins to use, defaults to Criteria::LEFT_JOIN
	 * @return     array Array of ProspectMeta objects.
	 * @throws     PropelException Any exceptions caught during processing will be
	 *		 rethrown wrapped into a PropelException.
	 */
	public static function doSelectJoinProspectTipo(Criteria $criteria, $con = null, $join_behavior = Criteria::LEFT_JOIN)
	{
		$criteria = clone $criteria;

		// Set the correct dbName if it has not been overridden
		if ($criteria->getDbName() == Propel::getDefaultDB()) {
			$criteria->setDbName(self::DATABASE_NAME);
		}

		ProspectMetaPeer::addSelectColumns($criteria);
		$startcol = (ProspectMetaPeer::NUM_COLUMNS - ProspectMetaPeer::NUM_LAZY_LOAD_COLUMNS);
		ProspectTipoPeer::addSelectColumns($criteria);

		$criteria->addJoin(ProspectMetaPeer::PROSPECT_TIPO_ID, ProspectTipoPeer::ID, $join_behavior);

		$stmt = BasePeer::doSelect($criteria, $con);
		$results = array();

		while ($row = $stmt->fetch(PDO::FETCH_NUM)) {
			$key1 = ProspectMetaPeer::getPrimaryKeyHashFromRow($row, 0);
			if (null !== ($obj1 = ProspectMetaPeer::getInstanceFromPool($key1))) {
				// We no longer rehydrate the object, since this can cause data loss.
				// See http://propel.phpdb.org/trac/ticket/509
				// $obj1->hydrate($row, 0, true); // rehydrate
			} else {

				$cls = ProspectMetaPeer::getOMClass(false);

				$obj1 = new $cls();
				$obj1->hydrate($row);
				ProspectMetaPeer::addInstanceToPool($obj1, $key1);
			} // if $obj1 already loaded

			$key2 = ProspectTipoPeer::getPrimaryKeyHashFromRow($row, $startcol);
			if ($key2 !== null) {
				$obj2 = ProspectTipoPeer::getInstanceFromPool($key2);
				if (!$obj2) {

					$cls = ProspectTipoPeer::getOMClass(false);

					$obj2 = new $cls();
					$obj2->hydrate($row, $startcol);
					ProspectTipoPeer::addInstanceToPool($obj2, $key2);
				} // if obj2 already loaded
				
				// Add the $obj1 (ProspectMeta) to $obj2 (ProspectTipo)
				$obj2->addProspectMeta($obj1);

			} // if joined row was not null

			$results[] = $obj1;
		}
		$stmt->closeCursor();
		return $results;
	}


